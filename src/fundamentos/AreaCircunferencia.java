package fundamentos;

public class AreaCircunferencia {
	
	// Variáveis e constantes
	public static void main(String[] args) {
		double raio = 3.4;
		final double PI = 3.14159;
		
		double area = PI * raio * raio;
		
		raio = 10;
		area = PI * raio * raio;
		
		System.out.println("Área = " + area + "m2.");
		
		// Sintaxe:
		//<tipo> <nome> = <valor incial>;
		// Exemplos:
	}
}
